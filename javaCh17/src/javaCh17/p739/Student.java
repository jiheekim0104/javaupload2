package javaCh17.p739;

public class Student {
	private String name;
	private int score;
	private double sight;
	public Student(String name, int score,double sight) {
		super();
		this.name = name;
		this.score = score;
		this.sight = sight;
	}
	public String getName() {
		return name;
	}
	public int getScore() {
		return score;
	}
	public double getSight() {
		return sight;
	}
	
	
	
	
}
